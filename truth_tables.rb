# AND Table
# Symbol: &&

puts "AND table"
puts "true and true: #{true && true}"
puts "true and false: #{true && false}"
puts "false and true: #{false && true}"
puts "false and false: #{false && false}"


# OR Table
# Symbol: ||
puts "true or true: #{true || true}"
puts "true or false: #{true || false}"
puts "false or true: #{false || true}"
puts "false or false: #{false || false}"

# NOT Table
# Symbol: !
puts "not true: #{!true}"
puts "not false: #{!false}"